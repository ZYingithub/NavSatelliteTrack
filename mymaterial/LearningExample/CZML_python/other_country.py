    ps = czml.Position()
    ps._cartographicDegrees = [other_lng[i], other_lat[i], 2000]
    packet_ele.position = ps
    # 最方便的是，对json格式的最大支持，CZML的任何属性都可以只是一个json字符串
    #定义位置的另一种方法
    #packet2.position = {'cartographicDegrees': [116, 39, 2000]}
    # 添加Label
    # 小灯泡能将粘贴的字符串 变成字典型，识别其格式
    lb = czml.Label()
    lb.scale = 1.0
    lb.horizontalOrigin = "LEFT"
    lb.show = "true"
    lb.text = other_array[i]
    packet_ele.label = lb
    doc.packets.append(packet_ele)



